"""Legal and regulatory compliance monitoring for AI edtech.

Monitor AI regulatory developments, FERPA guidance, COPPA, EU AI Act
implementation, and surface anything affecting Aelu's architecture or
data practices. Built for an AI governance attorney — precision matters.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from datetime import datetime, timezone, UTC
from enum import Enum
from typing import Optional

logger = logging.getLogger(__name__)


class RegulatoryFramework(Enum):
    EU_AI_ACT = "eu_ai_act"
    GDPR = "gdpr"
    FERPA = "ferpa"
    COPPA = "coppa"
    CPRA = "cpra"
    FTC_AI = "ftc_ai"
    STATE_AI_LAWS = "state_ai_laws"
    NIST_AI_RMF = "nist_ai_rmf"
    UNESCO_AI_ETHICS = "unesco_ai_ethics"
    OECD_AI_PRINCIPLES = "oecd_ai_principles"
    UK_AI_REGULATION = "uk_ai_regulation"


@dataclass
class ComplianceSurface:
    area: str
    description: str
    frameworks: list[RegulatoryFramework]
    risk_level: str  # low, medium, high
    current_controls: list[str]
    data_types: list[str]


@dataclass
class FeedSource:
    name: str
    url: str
    feed_type: str  # rss, api, scrape
    framework: RegulatoryFramework
    check_frequency_hours: int = 24


@dataclass
class ActionItem:
    title: str
    description: str
    framework: RegulatoryFramework
    urgency: str  # low, medium, high, critical
    deadline: str | None = None
    assigned_to: str = "owner"


@dataclass
class SurfaceAssessment:
    surface: ComplianceSurface
    status: str  # compliant, gap, unknown
    gaps: list[str] = field(default_factory=list)
    recommendations: list[str] = field(default_factory=list)


@dataclass
class ComplianceReport:
    overall_status: str  # compliant, attention_needed, action_required
    surfaces: list[SurfaceAssessment] = field(default_factory=list)
    action_items: list[ActionItem] = field(default_factory=list)
    last_checked: str = ""

    def __post_init__(self):
        if not self.last_checked:
            self.last_checked = datetime.now(UTC).strftime("%Y-%m-%d %H:%M:%S")


@dataclass
class ImpactAssessment:
    affects_aelu: bool
    impact_level: str  # none, low, medium, high
    affected_surfaces: list[str]
    required_changes: list[str]
    timeline: str
    confidence: float  # 0-1


