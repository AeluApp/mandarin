class AeluComplianceSurface:
    """Map Aelu's regulatory surfaces."""

    def get_surfaces(self) -> list[ComplianceSurface]:
        return list(_SURFACES)

    def get_by_framework(self, framework: RegulatoryFramework) -> list[ComplianceSurface]:
        return [s for s in _SURFACES if framework in s.frameworks]

    def get_high_risk(self) -> list[ComplianceSurface]:
        return [s for s in _SURFACES if s.risk_level == "high"]


